package drevo;

public class Person {
	String name; 
	int date_of_birth;
	String date_of_dead;
	String mom;
	String father;
	public  Person(String name, int date_of_birth, String date_of_dead, String mom,String father){
		this.name = name;
		this.date_of_birth = date_of_birth;
		this.date_of_dead = date_of_dead;
		this.mom = mom;
		this.father = father;
	}

}
